let currentSongIndex = 0;
let isPlaying = false;
let audioPlayer = null;

function openTab(tab) {
  const content = document.getElementById("content");
  if (!content) return;

  if (tab === "audio") {
    // Generate playlist track rows dynamically using your existing structure
    const playlistHTML = spotifyPlaylist.map((song, index) => `
      <div class="song ${index === currentSongIndex ? 'active' : ''}" onclick="selectSong(${index})">
        <strong>${song.title}</strong> - ${song.artist}
      </div>
    `).join('');

    content.innerHTML = `
      <div class="music-player">
        <h2>🎧 My Playlist</h2>
        
        <!-- Album Cover Frame -->
        <img id="display-cover" class="spotify-cover" src="${spotifyPlaylist[currentSongIndex].cover}" onerror="this.src='https://placehold.co🎵'">
        
        <div class="song-metadata">
          <h3 id="display-title">${spotifyPlaylist[currentSongIndex].title}</h3>
          <p id="display-artist">${spotifyPlaylist[currentSongIndex].artist}</p>
        </div>

        <!-- Smart Karaoke Lyric Frame -->
        <div id="lyrics-box" class="lyrics-window"></div>

        <!-- Custom Controls Frame -->
        <div class="player-controls">
          <button class="ctrl-btn" onclick="prevSong()">⏮️</button>
          <button id="master-play-btn" class="ctrl-btn main-play" onclick="togglePlay()">▶️</button>
          <button class="ctrl-btn" onclick="nextSong()">⏭️</button>
        </div>

        <!-- Main Hidden Core Audio Engine -->
        <audio id="player" src="${spotifyPlaylist[currentSongIndex].file}"></audio>
      </div>

      <!-- Playlist Selection Stack below player -->
      <div class="playlist-container" style="max-width: 320px; margin: 20px auto 0 auto;">
        ${playlistHTML}
      </div>
    `;

    // Instantiate engine links and build views
    audioPlayer = document.getElementById("player");
    loadLyrics(currentSongIndex);
    attachEngineListeners();
  }
}
function selectSong(index) {
  currentSongIndex = index;
  const song = spotifyPlaylist[index];

  // Refresh visual elements inside target containers
  document.getElementById("display-title").innerText = song.title;
  document.getElementById("display-artist").innerText = song.artist;
  document.getElementById("display-cover").src = song.cover;

  // Toggle active styling highlights across rows
  document.querySelectorAll(".song").forEach((item, i) => {
    item.classList.toggle("active", i === index);
  });

  loadLyrics(index);
  
  audioPlayer.src = song.file;
  playAudio();
}

function togglePlay() {
  if (isPlaying) {
    pauseAudio();
  } else {
    playAudio();
  }
}

function playAudio() {
  audioPlayer.play();
  isPlaying = true;
  document.getElementById("master-play-btn").innerText = "⏸️";
}

function pauseAudio() {
  audioPlayer.pause();
  isPlaying = false;
  document.getElementById("master-play-btn").innerText = "▶️";
}

function nextSong() {
  // Loop back to track 0 automatically if at the end of playlist array
  let nextIndex = currentSongIndex + 1;
  if (nextIndex >= spotifyPlaylist.length) {
    nextIndex = 0;
  }
  selectSong(nextIndex);
}

function prevSong() {
  // Jump back to trailing list item if preceding index drop breaks zero boundary
  let prevIndex = currentSongIndex - 1;
  if (prevIndex < 0) {
    prevIndex = spotifyPlaylist.length - 1;
  }
  selectSong(prevIndex);
}

function loadLyrics(index) {
  const lyricsBox = document.getElementById("lyrics-box");
  const lyricsData = spotifyPlaylist[index].lyrics;

  lyricsBox.innerHTML = lyricsData.map((line, i) => `
    <div class="lyric-row" id="lyric-row-${i}">
      ${line.text}
    </div>
  `).join('');
}

function attachEngineListeners() {
  // 1. Listen for runtime changes to process timed lyric updates
  audioPlayer.addEventListener("timeupdate", () => {
    const currentTime = audioPlayer.currentTime;
    const lyricsData = spotifyPlaylist[currentSongIndex].lyrics;
    let targetIndex = -1;

    for (let i = 0; i < lyricsData.length; i++) {
      if (currentTime >= lyricsData[i].time) {
        targetIndex = i;
      } else {
        break;
      }
    }

    if (targetIndex !== -1) {
      document.querySelectorAll(".lyric-row").forEach(row => row.classList.remove("current-lyric"));
      const currentLineNode = document.getElementById(`lyric-row-${targetIndex}`);
      if (currentLineNode) {
        currentLineNode.classList.add("current-lyric");
        
        // Handle smooth scrolling for lyrics
        const lyricsBox = document.getElementById("lyrics-box");
        lyricsBox.scrollTop = currentLineNode.offsetTop - lyricsBox.offsetTop - 30;
      }
    }
  });

  // 2. Continuous Play Transition Listener
  audioPlayer.addEventListener("ended", () => {
    nextSong(); // Triggers next song when current one finishes
  });
}
